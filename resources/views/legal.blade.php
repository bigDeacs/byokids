<p><strong>Frrequently Asked Questions</strong></p>

<h4>Q. What is included in my cruise fare?</h4>

<p>Accommodation, all main meals, entertainment and the use of the ship’s many facilities such as swimming pools, spas, gym, steam rooms etc.</p>

<h4>Q. Where on the ship is usually the most stable location to have your cabin?</h4>

<p>Midship.</p>

<h4>Q. What do I wear on board?</h4>

<p>Depending on the class of your ship casual clothes are generally suitable during the day. Late afternoon, smart casual clothing is required in public areas, including the restaurants. Some nights on the ship are themed, so be sure to check out the cruise line website for typical theme nights. Generally one night will be formal night.</p>

<h4>Q. What is the currency on board?</h4>

<p>What currency is used on board depends on the origin of the ship and the itinerary. Your consultant will be able to tell you at the time you make a booking.</p>

<h4>Q. If I get sea sick, is there anything that will help me with this?</h4>

<p>Larger cruise lines have stabilizers which help with the motion of a cruise ship in the ocean. It is also recommended that you take sea sickness tablets with you. If you still experience motion sickness, the on board doctor will be able to assist.</p>

<h4>Q. Do I have to tip on board?</h4>

<p>Most cruise lines include gratuities in your cruise cost. However in the instance it is not included, it is customary to tip staff at the end of your cruise, just to thank them for their excellent service on board. This is not compulsory; however it is a nice gesture towards the cruise staff.</p>

<h4>Q. Do I need Travel Insurance for my Cruise trip?</h4>

<p>Definitely! You should not assume ‘it won’t happen to me’ – accidents or illness can happen to anyone. We also recommend taking out your insurance at the point of booking your cruise, this will ensure cover from the point you pay a deposit to the point you arrive home.</p>

<h4>Q. Are there Visas and vaccinations required for my cruise?</h4>

<p>We always recommend enquiring about Visa/Vaccination requirements for your destination of travel, upon booking your cruise. Alternatively you can view requirements online at www.passports.gov.au or if you are a New Zealand citizen refer to&nbsp;<a href="http://www.passports.govt.nz/" target="_blank">www.passports.govt.nz</a></p>

<h4>Q. I have to fly to a city to pick up my cruise, what time should I arrive prior to the cruise?</h4>

<p>We recommend ensuring your flight arrives no later than 4 hours before your cruise departure time, if not arrive the day prior. This will ensure ample time and that you do not miss your cruise! When flying home after your cruise, ensure your flight departs no earlier than 5 hours after your cruise arrives back in port. Also ensure you double check your arrival/departure times of your flights a day before you depart as airlines can change times or dates of the flights.</p>

<h4>Q. I need medication on board, can I take this with me?</h4>

<p>Yes, all medical conditions and medication needs to be advised at the time of making your booking. It is also advised to take a few extra days’ supply, in case of any unforeseen delays. There is also a doctor on board if any medical attention is needed.</p>

<h4>Q. When will I receive my cruise tickets?</h4>

<p>Provided we have received all of your personal details and you have completed your online cruise registration, along with paid your booking in full, then your cruise tickets and information should arrive within 3 weeks prior to your sail date. Some cruise lines will only issue boarding passes, however each cruise line is different. This is a question to ask at the time your final balance is paid.</p>

<h4>Q. How do I find out about shore tours available in each port?</h4>

<p>Most cruise lines require you to have registered yourself on their website prior to being able to view available shore tours. Most shore tour information can be found online at the cruise website, some cruise lines even let you pre book these tours. This is not a bad idea and saves you time, whilst ensuring you do not miss out!</p>

